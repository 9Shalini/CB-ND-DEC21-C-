#include <iostream>
using namespace std;
int main(){
	// compound assignment opr
	int a=10;
	// a=a+100;//110


	a+=100;

	cout<<a<<endl;//110

	int b=90;
	// b=b-20;
	b-=20;

	cout<<b<<endl;//70


	int c=3;
	// c=c*4;
	c*=4;
	cout<<c<<endl;//12

	int d=60;
	// d=d/10;
	d/=10;

	cout<<d<<endl;//6







	return 0;
}